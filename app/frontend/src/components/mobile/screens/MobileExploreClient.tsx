// Get features for explore tab
const exploreFeatures = FEATURE_INVENTORY.filter(
  (f) => f.category === "explore" || f.category === "plan"
);

export function MobileExploreClient() {
  return (
    <div className={styles.container}>
      <header className={styles.header}>
        <h1 className={styles.title}>Explore</h1>
        <p className={styles.subtitle}>Browse all modules</p>
      </header>

      <div className={styles.grid}>
        {exploreFeatures.map((feature) => (
          <FeatureCard key={feature.key} feature={feature} />
        ))}
      </div>
    </div>
  );
}

function FeatureCard({ feature }: { feature: FeatureDefinition }) {
  const icon = ICONS[feature.key] || (
    <svg width="24" height="24" viewBox="0 0 24 24" fill="none" stroke="currentColor" strokeWidth="1.75">
      <circle cx="12" cy="12" r="10" />
    </svg>
  );

  return (
    <Link href={feature.href} className={styles.card}>
      <div className={styles.cardIcon}>{icon}</div>
      <div className={styles.cardContent}>
        <h3 className={styles.cardTitle}>{feature.label}</h3>
        <p className={styles.cardDescription}>{feature.description}</p>
      </div>
    </Link>
  );
}

